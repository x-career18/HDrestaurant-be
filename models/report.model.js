import mongoose from "mongoose";

const ReportSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ReportModel = mongoose.model("Report", ReportSchema);

export default ReportModel;
